#import "NYTGenreListTableViewController.h"
#import "NYTBook.h"
#import "NYTAPIClient.h"

@interface NYTGenreListTableViewController ()

@property (nonatomic, strong) NYTAPIClient *apiClient;

@end

@implementation NYTGenreListTableViewController

- (id)initWithStyle:(UITableViewStyle)style
{
    self = [super initWithStyle:style];
    if (self) {
        // Custom initialization
    }
    return self;
}

- (void)viewDidLoad
{
    [super viewDidLoad];
    NSLog(@"Genre: %@",self.genreKey);
    self.apiClient = [[NYTAPIClient alloc]init];
    [self.apiClient getGenreList:self.genreKey WithCompletion:^(NSArray *books) {
            self.genreList = books;
            NSLog(@"%@",self.genreList);
            [self.tableView reloadData];
    }];
    

    // Uncomment the following line to preserve selection between presentations.
    // self.clearsSelectionOnViewWillAppear = NO;
    
    // Uncomment the following line to display an Edit button in the navigation bar for this view controller.
    // self.navigationItem.rightBarButtonItem = self.editButtonItem;
